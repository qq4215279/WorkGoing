/**
 * 知识点：
 * 泛型 -- Generic
 * 泛型：可以在类或方法中预支地使用未知的类型。
 * tips:一般在创建对象时，将未知的类型确定具体的类型。当没有指定泛型时，默认类型为Object类型。
 *
 * 使用泛型的好处：
 *   1. 将运行时期的ClassCastException，转移到了编译时期变成了编译失败。
 *   2. 避免了类型强转的麻烦。
 * tips:泛型是数据类型的一部分，我们将类名与泛型合并一起看做数据类型。
 *
 */
